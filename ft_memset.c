#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*bucket;

	i = 0;
	bucket = (unsigned char *)b;
	while (i < len)
	{
		bucket[i] = c;
		i++;
	}
	return (b = bucket);
}
